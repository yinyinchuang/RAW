            <p>RAW 提供 7 天鑑賞期，若需辦理退換貨，請注意下列事項：</br>
                1. 鑑賞期非試用期，若您收到商品經檢視後有任何不合意之處請勿拆開使用，並立即辦理退換貨，商品退換貨需在收到商品後 7 天內告知並寄回，逾期恕不受理。</br>
                2. 請務必先來信聯絡我們（service@raw.com.tw），提供訂購人姓名 / 訂單編號 / 聯絡電話 / 退換商品名，並註明欲「退貨」或「換貨」及退換原因。</br>
                3. 退換貨請保持商品的原始狀態（無異味髒污、磨損毀壞、加工改造等）與包裝完整 （內外包裝、封膜、封口、隨貨文件、保固卡、贈品等）。</br>
                4. 除了商品瑕疵以外，若商品已非全新狀態與完整包裝，可能需負擔部分費用或影響退貨權力之行使。</br>
                5. 因個人因素（商品和您的預期有出入）換貨請自行寄回，另將酌收 80
                元物流處理費（此為換貨商品重新配送所需的處理費，國外寄送將另行報價），請於換貨包裹內附上處理費一併寄回，以便我們為您處理。更換商品以一次為限，如遇商品缺貨或其他因素無法出貨的情況則退款處理。</br>
                6. 因個人因素（商品和您的預期有出入）退貨，若為整筆訂單退貨且自行寄回，將退款原消費總額。</br>
                7. 承上，若為部分退貨，假使退貨後未滿優惠資格（例如：滿額免運、多件折扣），將從退款直接扣除其中差價。
                8. 欲退貨之商品，若為組合出售的促銷商品，不可單品退貨，需將整個商品組合一起退回。</br>
                9. 請勿使用商品包裝作為退貨包裝（例如：防塵袋、夾鏈袋），如造成商品包裝損毀，將視為商品已使用無法退貨，恕無法受理退貨。</br>
                10. 若商品完整性不齊全或是因包裝不妥導致寄回有破損或損壞，恕不退換或退款，敬請見諒。</br>
                11. 特殊活動之商品（如 POP-UP 快閃店、預購活動等），訂單一旦成立，則不提供因顧客個人因素退換貨，訂購前請確認顏色、尺寸、數量。</br>
                </br>
