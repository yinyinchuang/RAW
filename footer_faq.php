                門市時，將會發送簡訊通知您，待商品到貨後至門市取貨時直接付款即可。</br>
                2. 信用卡線上刷卡</br>
                RAW所使用為綠界科技刷卡系統，信用卡類型（VISA、MasterCard、JCB、銀聯卡 UnionPay card）皆可使用。</br>
                ※ 訂單成立之後請於一小時內進行付款作業，否則系統會自動取消沒有收到款項的訂單 ※</br>
                3. ATM 虛擬帳戶匯款</br>
                完成結帳時將取得當次訂單的專屬虛擬帳號（請確認務必取得繳費帳號），取得帳號後於一日內前完成轉帳，若未能完成；系統便會自動取消您此次訂單。</br>
                ATM 虛擬帳戶匯款一筆訂單只會產生一組獨特匯款號碼，安全性更好，請放心使用。</br>
                ※ 系統將會於訂單資訊處顯示匯款期限，若未能於期限內完成轉帳或未取得繳費帳號，系統將自動取消您該次的訂單 ※</br>
                </br>
                Q. 沒有在領取的期限內取貨怎麼辦？</br>
                若您逾期未完成門市/宅配配送取貨時，商品將會退回本公司，訂單亦將自動取消無法重新配送，如您仍需要該商品，請再次重新上網訂購。</br>
                當包裹出貨完成未被領取或拒收退回，該訂購帳號若累計達三次將無法使用此配送方式。</br>
                ※新竹物流及 7-11 超商取件未領取紀錄獨立計算</br>
                </br>
